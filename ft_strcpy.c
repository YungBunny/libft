#include "libft.h"

char		*ft_strcpy(char *dst, const char *src)
{
	int i;

	i = 0;
	if (src)
	{
		while (src[i])
		{
			dst[i] = src[i];
			i++;
		}
	}
	dst[i] = src[i];
	return (dst);
}
