#include "libft.h"

size_t		ft_strtrime(char const *s)
{
	size_t i;

	i = ft_strlen(s) - 1;
	if (s)
	{
		while (s[i] == ' ' || s[i] == '\n' || s[i] == '\t')
			i--;
	}
	return (i);
}
