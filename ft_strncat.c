#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*str;
	size_t	i;
	size_t	j;

	str = s1;
	i = 0;
	j = 0;
	while (str[i])
		i++;
	while (s2[j] && n > j)
		str[i++] = s2[j++];
	str[i] = '\0';
	return (s1);
}
