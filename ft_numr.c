#include "libft.h"

size_t		ft_numr(char const *s, char c)
{
	size_t r;

	r = 0;
	while (*s)
	{
		if (*s != c)
		{
			r++;
			s++;
			while (*s != c)
			{
				s++;
				if (*s == '\0')
					return (r);
			}
		}
		else
		{
			while (*s == c)
				s++;
		}
	}
	return (r);
}
